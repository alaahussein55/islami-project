        val recyclerview =view.findViewById<RecyclerView>(R.id.suranamerecyc)
        adapter= sura_name_adapter(suranames)
        adapter.oncitemlick= object :sura_name_adapter.onclick{
            override fun onsuraname(sura: String, position: Int) {
              //val filename ="${position+1}.txt"
